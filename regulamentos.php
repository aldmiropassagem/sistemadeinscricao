  <h1>Termo de Compromisso do Estudante</h1>
  <br> <br> 
<p> A fim de um ambiente sadio dentro do Colégio,  é necessário que o estudante
    não só leia mas, cumpra e aceite os princípios normativos da filosofia da 
    <a href="index.php">instituição,</a>com vista ao desenvolvimento harmonioso e assim evitando tudo que é
    nocivo e capaz de desmoronar o aspecto físico e social da personalidade humana.</p> <br> <br>
 <h4> De recordar que para <a href="inscrever.php">novas inscrições</a> o candidato deve obrigatóriamente aceitar os termos e condições impostas pela instituição. Assim sendo, eis então os direitos e deveres à aceitar:</h4>
<br> <br> <br>
<article id="deveres">
 <p>1.1 Abster-se de qualquer tendência fraudulenta, mormente: financeira, mental, moral e outras não menos importantes.</p> <br>
<p> 1.2 Evitar o estado de embriaguez e o uso de bebidas alcoólicas, estupefaciente e outras drogas capaz de alterar o normal funcionamento do indivíduo.</p> <br>
<u>1.a) Evitar o uso de roupas extravagantes e indecentes, tais como: calções, calças esaias a baixo da centura, vestidos, blusas, saias e qualquer vestimenta de tamanho curto e igualmente vestimentas com rasgos exagerados.</u> <br><br>
<u>1.b) Evitar cortes extravagantes ou exagerados.</u> <br> <br>
<u>1.c) Evitar qualquer tipo de comportamento capaz de manchar a imagem da <a href="index.php">instituição</a>.</u> <br> <br>
<u>1.d) Manter o telemóvel desligado ou em silêncio enquanto estiver em aula.</u> <br> <br>
<p>1.3 Todo o acto tendente a insubordinação aos membros da direção, professores, colegas, outros funcionários da instituição, com carácter ameaçador tanto fora como dentro da instituição. Será punível e susceptível de expulsão imediata.</p> <br>
<p>2.1 Respeitar os membros da direção, professores, os colegas e os restantes funcionários da instituição.</p> <br>
<p>2.2 Partilhar ideias úteis, de caractér educacional e social com os colegas professores.</p> <br>
<p>2.3 Participar das reuniões de caractér pedagógico oficialmente organizadas.</p> <br>
<p>2.4 Participar das <a href="atividades.php">atividades extra escolar</a> de caractér desoportivas, educacional e social.</p> <br>
<p>2.5 Participar das equipas desoportivas inter-turmas.</p> <br>
<p>2.6 Assiduidade nas assistências as aulas, esatar consciente das responsabilidades educacionais e ser pontual.</p> <br>
<p>3.1 Contribuir  para  o asseio, concervação e preservação dos materiais próprios da instituição escolar.</p> <br>
<p>3.2 Regularidade e fidelidade no pagamento das propinas mensais e outros pagamentos à instituição.</p> <br>
<u>3.a) O pagamento de propina é de carácter obrigatótio e deverá ser feito na primeira quinzena de cada mês útil ao lectivo.</u>  <br> <br>
<u>3.b) Toda propina paga depois da primeira quinzena do mês, será susceptível a um pagamento adicional (multa).</u>  <br> <br>
<u>3.c) O pagamento da propina deverá ser feito de modo automático (depósitos ou transferências bancárias).</u> <br> <br>
<u>3.d) O comprovativo bancário de pagamento à instituição, deve ser mantido em  posse até os fins próprios.</u>  <br> <br>
<u>3.e) A instituição está isenta de qualquer responsabilização no caso da perca do pelo estudante do comprovativo bancário ou de outra justificativa de pagamento.</u> <br> <br>
<p>3.3 O uso do cartão de estudante e da bata ou uniforme escolar, é de carácter obrigatório.</p> <br>
<p>3.4 Não haverá devolução dos valores dados no acto da matrícula. </p>

