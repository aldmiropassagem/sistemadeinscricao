<p>O Complexo Escolar Privado Onilka ou Colégio Onilka, é uma instituição privada localizada na província de Benguela, município do Lobito, bairro Alto-Esperança junto a moagem.
A instituição dista à 200 metros da estrada nacional número 100, sentido oposto para Luanda-Benguela. A instituição  entrou em funcionamento na metade da 
década de 2010, aumentando assim o leque de instituições privadas do ensino médio e técnico no município, dando mais oportunidades e sendo detsa forma abrangente.</p> 
<br><br>
<p>Desde a sua fundação (2015), a instituição tem apostado fortemente na prata local (nascidos ou formados na província),
tanto que grande parte do corpo docente é formado maioritáriamente por jovens, capazes, compentes e com experiência
nas áreas destacadas. O o modo inovador, a acessibilidade o ensino de qualidade e comodidade têm sido dentre outros
fatores os que mais atrai novos inscritos.</p> 
<br><br>
<p>Atualmente a instituição se dastaca entre as instituições privadas do ramo educação com  os preços mais acessíveis do mercado local sem contar as diversidades
no quesito pagamento, que acaba por dar ao estudante mais alternativas e confiança nos pagamentos.</p> 
